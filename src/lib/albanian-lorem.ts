const LOREM_START = "Lorem ipsum dolor sit amet, consectetur adipiscing elit. ";

export function generateLoremIpsum(options: {
  language: "latin" | "albanian";
  type: "words" | "sentences" | "paragraphs";
  count: number;
  startWithLorem: boolean;
  includeHtml: boolean;
}): string {
  const { language, type, count, startWithLorem, includeHtml } = options;
  const words = language === "latin" ? latinWords : albanianWords;
  const sentences = language === "latin" ? latinSentences : albanianSentences;

  const pick = <T,>(arr: T[]): T => arr[Math.floor(Math.random() * arr.length)];

  const generateSentence = (): string => pick(sentences);

  const generateParagraph = (): string => {
    const sentenceCount = 4 + Math.floor(Math.random() * 4);
    const result: string[] = [];
    for (let i = 0; i < sentenceCount; i++) {
      result.push(generateSentence());
    }
    return result.join(" ");
  };

  let output = "";

  if (type === "words") {
    const result: string[] = [];
    for (let i = 0; i < count; i++) {
      result.push(pick(words));
    }
    output = result.join(" ");
    if (startWithLorem && language === "latin") {
      output = "Lorem ipsum " + output.split(" ").slice(2).join(" ");
    }
  } else if (type === "sentences") {
    const result: string[] = [];
    for (let i = 0; i < count; i++) {
      result.push(generateSentence());
    }
    output = result.join(" ");
    if (startWithLorem && language === "latin") {
      output = LOREM_START + result.slice(1).join(" ");
    }
  } else {
    const paragraphs: string[] = [];
    for (let i = 0; i < count; i++) {
      let p = generateParagraph();
      if (i === 0 && startWithLorem && language === "latin") {
        p = LOREM_START + p;
      }
      paragraphs.push(p);
    }
    if (includeHtml) {
      output = paragraphs.map((p) => `<p>${p}</p>`).join("\n\n");
    } else {
      output = paragraphs.join("\n\n");
    }
  }

  return output;
}
